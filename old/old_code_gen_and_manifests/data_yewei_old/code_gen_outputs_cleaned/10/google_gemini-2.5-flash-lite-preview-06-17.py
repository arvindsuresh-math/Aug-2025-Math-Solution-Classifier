# Calculate the number of people on the first ship
total_people = 847
# Let S be the number of people on the first ship.
# The second ship had 2*S people.
# The third ship had 2*(2*S) = 4*S people.
# The total number of people is S + 2*S + 4*S = 7*S.
# We know that 7*S = 847.
# So, S = 847 / 7.
people_on_first_ship = total_people / 7

# Print the result
print(people_on_first_ship)