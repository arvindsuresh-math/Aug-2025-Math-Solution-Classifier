# Calculate the total number of pencils Arnel shared with his friends
num_friends = 5
pencils_per_friend = 8
pencils_shared = num_friends * pencils_per_friend

# Calculate the total number of pencils Arnel had initially
pencils_kept = 10
total_pencils = pencils_shared + pencils_kept

# Calculate the number of pencils in each box
num_boxes = 10
pencils_per_box = total_pencils / num_boxes

# The final answer is the number of pencils per box
result = pencils_per_box