def solve():
    """Index: 2135.
    Returns: the average weight of Janele's cats in pounds.
    """
    # L1
    cat1_weight = 12 # first cat weighs 12 pounds
    cat2_weight = 12 # second cat weighs 12 pounds
    cat3_weight = 14.7 # third cat weighs 14.7 pounds
    cat4_weight = 9.3 # fourth cat weighs 9.3 pounds
    total_weight = cat1_weight + cat2_weight + cat3_weight + cat4_weight

    # L2
    num_cats = 4 # She has 4 of them
    average_weight = total_weight / num_cats

    # FA
    answer = average_weight
    return answer