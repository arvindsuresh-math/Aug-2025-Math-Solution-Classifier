def solve():
    """Index: 2019.
    Returns: Alma's score in the test.
    """
    # L1
    melina_age = 60 # she is 60
    age_multiplier = 3 # three times as old as Alma
    alma_age = melina_age / age_multiplier

    # L2
    sum_of_ages = melina_age + alma_age

    # L3
    score_multiplier = 2 # twice the total number of points
    alma_score = sum_of_ages / score_multiplier

    # FA
    answer = alma_score
    return answer