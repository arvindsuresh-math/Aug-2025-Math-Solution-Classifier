def solve():
    """Index: 1047.
    Returns: Irene's age.
    """
    # L1
    eddie_age = 92 # Eddie is 92 years old
    younger_factor = 4 # four times younger than he is
    becky_age = eddie_age / younger_factor

    # L2
    older_factor = 2 # two times her age
    irene_age = becky_age * older_factor

    # FA
    answer = irene_age
    return answer