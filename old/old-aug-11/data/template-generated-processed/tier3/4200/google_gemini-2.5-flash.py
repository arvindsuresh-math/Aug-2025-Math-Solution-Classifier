def solve():
    """Index: 4200.
    Returns: the total number of mangoes all of them have combined.
    """
    # L1
    alexis_mangoes = 60 # If Alexis has 60 mangoes
    alexis_multiple = 4 # four times as many mangoes
    dilan_ashley_combined_mangoes = alexis_mangoes / alexis_multiple

    # L2
    total_mangoes = dilan_ashley_combined_mangoes + alexis_mangoes

    # FA
    answer = total_mangoes
    return answer