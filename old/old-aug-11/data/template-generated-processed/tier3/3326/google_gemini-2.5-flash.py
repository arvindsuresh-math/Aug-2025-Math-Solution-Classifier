def solve():
    """Index: 3326.
    Returns: the weight of the blue whale's tongue in tons.
    """
    # L2
    tongue_weight_pounds = 6000 # The tongue of an adult blue whale can weigh 6000 pounds
    pounds_per_ton = 2000 # If one ton is 2000 pounds
    tongue_weight_tons = tongue_weight_pounds / pounds_per_ton

    # FA
    answer = tongue_weight_tons
    return answer