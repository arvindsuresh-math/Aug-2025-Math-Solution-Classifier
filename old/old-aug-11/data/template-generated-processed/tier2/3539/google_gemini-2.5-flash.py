def solve():
    """Index: 3539.
    Returns: the number of candles on Rupert's cake.
    """
    # L1
    peter_candles = 10 # 10 candles on his cake

    # L2
    rupert_age_factor = 3.5 # 3 and 1/2 times older than Peter
    rupert_candles = rupert_age_factor * peter_candles

    # FA
    answer = rupert_candles
    return answer