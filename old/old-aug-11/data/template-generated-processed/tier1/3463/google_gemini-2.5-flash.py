def solve():
    """Index: 3463.
    Returns: the total amount spent on cards.
    """
    # L1
    christmas_cards = 20 # 20 for Christmas gifts
    birthday_cards = 15 # 15 for birthday gifts
    total_cards = christmas_cards + birthday_cards

    # L2
    cost_per_card = 2 # each card cost $2
    total_spent = total_cards * cost_per_card

    # FA
    answer = total_spent
    return answer