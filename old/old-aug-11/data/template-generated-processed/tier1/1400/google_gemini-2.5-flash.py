def solve():
    """Index: 1400.
    Returns: Noah's age after 10 years.
    """
    # L1
    twice_multiplier = 2 # twice Joe's age
    joe_age = 6 # Joe is 6 years old
    noah_current_age = twice_multiplier * joe_age

    # L2
    years_after = 10 # after 10 years
    noah_future_age = noah_current_age + years_after

    # FA
    answer = noah_future_age
    return answer