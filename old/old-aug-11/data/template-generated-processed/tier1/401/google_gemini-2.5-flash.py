def solve():
    """Index: 401.
    Returns: the number of pounds of firewood collected by Ela.
    """
    # L1
    kimberley_firewood = 10 # Kimberley collects ten pounds of firewood
    houston_firewood = 12 # Houston collects 12 pounds of firewood
    kimberley_houston_total = kimberley_firewood + houston_firewood

    # L2
    total_firewood_collected = 35 # a total of 35 pounds of firewood
    ela_firewood = total_firewood_collected - kimberley_houston_total

    # FA
    answer = ela_firewood
    return answer