def solve(
    total_earned: int = 60,  # Lisa, Jack, and Tommy earned $60
):
    """Index: 36.
    Returns: how much more money Lisa earned than Tommy.
    """

    #: L1
    lisa_earned = total_earned * 1/2

    #: L2
    tommy_earned = 16.0

    #: L3
    difference = lisa_earned - tommy_earned

    #: FA
    answer = difference
    return answer