def solve():
    """Index: 633.
    Returns: the combined shoe size for Jasmine and Alexa.
    """
    # L1
    jasmine_shoe_size = 7 # Jasmine's shoe size is 7
    multiplier_twice = 2 # twice as big
    alexa_shoe_size = jasmine_shoe_size * multiplier_twice

    # L2
    combined_shoe_size = alexa_shoe_size + jasmine_shoe_size

    # FA
    answer = combined_shoe_size
    return answer