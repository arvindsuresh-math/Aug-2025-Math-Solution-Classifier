def solve():
    """Index: 5054.
    Returns: Jaymee's age.
    """
    # L1
    shara_age = 10 # Shara is 10 years old
    multiplier_twice = 2 # twice the age
    twice_shara_age = shara_age * multiplier_twice

    # L2
    older_than_twice = 2 # 2 years older than twice
    jaymee_age = twice_shara_age + older_than_twice

    # FA
    answer = jaymee_age
    return answer