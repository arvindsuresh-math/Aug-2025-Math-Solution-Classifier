def solve():
    """Index: 255.
    Returns: Matt's age 10 years from now.
    """
    # L1
    bush_age = 12 # Bush will be 12 years old
    bush_younger_by = 3 # Bush is younger than Matt by 3 years
    matt_age = bush_age + bush_younger_by

    # L2
    years_in_future = 10 # 10 years from now
    matt_age_in_10_years = years_in_future + matt_age

    # FA
    answer = matt_age_in_10_years
    return answer