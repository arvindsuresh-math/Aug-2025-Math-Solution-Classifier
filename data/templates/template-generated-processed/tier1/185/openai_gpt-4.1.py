def solve():
    """Index: 185.
    Returns: how many more trouts Caleb's dad caught compared to Caleb.
    """
    # L1
    caleb_trouts = 2 # Caleb caught 2 trouts
    dad_multiplier = 3 # three times as much as Caleb
    dad_trouts = caleb_trouts * dad_multiplier

    # L2
    difference = dad_trouts - caleb_trouts

    # FA
    answer = difference
    return answer