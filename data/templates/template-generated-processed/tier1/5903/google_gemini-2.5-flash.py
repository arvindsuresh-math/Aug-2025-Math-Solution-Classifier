def solve():
    """Index: 5903.
    Returns: the number of lattes sold.
    """
    # L1
    multiplier_four_times = 4 # four times the number of lattes
    teas_sold = 6 # If 6 teas were sold
    four_times_teas = multiplier_four_times * teas_sold

    # L2
    more_than_four_times = 8 # 8 more than four times
    lattes_sold = four_times_teas + more_than_four_times

    # FA
    answer = lattes_sold
