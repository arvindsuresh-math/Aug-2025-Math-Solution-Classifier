def solve():
    """Index: 6301.
    Returns: Randolph's age.
    """
    # L1
    sherry_age = 25 # Sherry is 25
    multiplier_for_twice = 2 # twice as old
    sydney_age = sherry_age * multiplier_for_twice

    # L2
    randolph_age_difference = 5 # 5 years older than Sydney
    randolph_age = sydney_age + randolph_age_difference

    # FA
    answer = randolph_age
    return answer