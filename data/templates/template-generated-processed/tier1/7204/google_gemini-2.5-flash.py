def solve():
    """Index: 7204.
    Returns: the total cost for entrance tickets.
    """
    # L1
    students = 20 # 20 students
    teachers = 3 # 3 teachers
    total_people = students + teachers

    # L2
    ticket_cost = 5 # $5 each
    total_cost = total_people * ticket_cost

    # FA
    answer = total_cost
    return answer