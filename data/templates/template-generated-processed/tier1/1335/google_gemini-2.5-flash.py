def solve():
    """Index: 1335.
    Returns: the total number of apples collected by Lexie and Tom.
    """
    # L1
    lexie_apples = 12 # Lexie picked 12 apples
    multiplier_for_twice = 2 # twice as many apples
    tom_apples = lexie_apples * multiplier_for_twice

    # L2
    total_apples = lexie_apples + tom_apples

    # FA
    answer = total_apples
    return answer