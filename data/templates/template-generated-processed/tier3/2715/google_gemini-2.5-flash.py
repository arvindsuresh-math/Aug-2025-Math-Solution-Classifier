def solve():
    """Index: 2715.
    Returns: the average number of birds each person sees.
    """
    # L1
    darrel_birds = 9 # Darrel sees 9 birds
    humphrey_birds = 11 # Humphrey sees 11 birds
    marcus_birds = 7 # Marcus sees 7 birds
    total_birds = darrel_birds + humphrey_birds + marcus_birds

    # L2
    number_of_people = 3 # Marcus, Humphrey, and Darrel are bird watching
    average_birds = total_birds / number_of_people

    # FA
    answer = average_birds
    return answer