def solve():
    """Index: 5886.
    Returns: the total number of students in the middle school.
    """
    # L2
    students_in_band = 168 # 168 students are in the band
    band_percentage = 20 # 20% of the students are in the band
    percentage_divisor = 100 # WK
    total_students = students_in_band / (band_percentage / percentage_divisor)

    # FA
    answer = total_students
