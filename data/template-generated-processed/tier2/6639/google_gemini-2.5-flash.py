def solve():
    """Index: 6639.
    Returns: the total cost Maria paid for the pen and the pencil.
    """
    # L1
    pencil_cost = 8 # The pencil cost her $8
    half_price_factor = 0.5 # half the price
    pen_cost = pencil_cost * half_price_factor

    # L2
    total_cost = pencil_cost + pen_cost

    # FA
    answer = total_cost
    return answer