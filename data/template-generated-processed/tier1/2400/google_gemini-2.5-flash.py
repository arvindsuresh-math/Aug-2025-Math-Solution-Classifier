def solve():
    """Index: 2400.
    Returns: the total amount Alexander spends.
    """
    # L1
    num_apples = 5 # 5 apples
    cost_per_apple = 1 # $1 each
    cost_apples = num_apples * cost_per_apple

    # L2
    num_oranges = 2 # 2 oranges
    cost_per_orange = 2 # $2 each
    cost_oranges = num_oranges * cost_per_orange

    # L3
    total_cost = cost_apples + cost_oranges

    # FA
    answer = total_cost
    return answer