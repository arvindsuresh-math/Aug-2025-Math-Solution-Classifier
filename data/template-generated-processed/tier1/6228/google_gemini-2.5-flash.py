def solve():
    """Index: 6228.
    Returns: the total number of apple trees planted by Ava and Lily.
    """
    # L1
    ava_planted = 9 # Ava planted 9 apple trees
    more_than_lily = 3 # 3 more than Lily planted
    lily_planted = ava_planted - more_than_lily

    # L2
    total_planted = ava_planted + lily_planted

    # FA
    answer = total_planted
    return answer