def solve():
    """Index: 586.
    Returns: the total number of miles Harvey and Sam ran together.
    """
    # L1
    sam_miles = 12 # Sam, who runs a total of 12 miles
    harvey_more_miles = 8 # Harvey runs 8 miles more than his friend Sam
    harvey_miles = sam_miles + harvey_more_miles

    # L2
    total_miles = harvey_miles + sam_miles

    # FA
    answer = total_miles
    return answer