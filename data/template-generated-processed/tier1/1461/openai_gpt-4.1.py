def solve():
    """Index: 1461.
    Returns: the area of the chalkboard in square feet.
    """
    # L1
    width = 3 # 3 feet wide
    length_multiplier = 2 # 2 times as long as it is wide
    length = length_multiplier * width

    # L2
    area = width * length

    # FA
    answer = area
    return answer