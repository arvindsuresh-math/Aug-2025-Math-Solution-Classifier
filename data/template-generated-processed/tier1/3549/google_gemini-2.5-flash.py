def solve():
    """Index: 3549.
    Returns: Matilda's age in years.
    """
    # L1
    jerica_multiplier = 2 # twice Louis' age
    louis_age = 14 # Louis is 14
    jerica_age = jerica_multiplier * louis_age

    # L2
    matilda_age_difference = 7 # 7 years older than Jerica
    matilda_age = jerica_age + matilda_age_difference

    # FA
    answer = matilda_age
    return answer