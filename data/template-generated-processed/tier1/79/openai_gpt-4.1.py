def solve():
    """Index: 79.
    Returns: the combined teaching experience of James and his partner in years.
    """
    # L1
    james_years = 40 # James spends 40 years teaching
    partner_less_years = 10 # His partner has been teaching for 10 years less
    partner_years = james_years - partner_less_years

    # L2
    combined_experience = james_years + partner_years

    # FA
    answer = combined_experience
    return answer