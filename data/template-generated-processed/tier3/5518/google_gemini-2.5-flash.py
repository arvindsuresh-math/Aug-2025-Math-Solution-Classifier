def solve():
    """Index: 5518.
    Returns: the number of Aaron's brothers.
    """
    # L1
    bennetts_brothers = 6 # Bennett has 6 brothers
    less_than_value = 2 # two less than
    twice_aarons_brothers = bennetts_brothers + less_than_value

    # L2
    divisor_for_half = 2 # WK
    aarons_brothers = twice_aarons_brothers / divisor_for_half

    # FA
    answer = aarons_brothers
    return answer