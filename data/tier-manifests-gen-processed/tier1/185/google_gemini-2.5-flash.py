def solve():
    """Index: 185.
    Returns: the number of more trouts Caleb's dad caught compared to Caleb.
    """
    # L1
    caleb_caught = 2 # Caleb caught 2 trouts
    multiplier_dad = 3 # three times as much as Caleb
    dad_caught = caleb_caught * multiplier_dad

    # L2
    difference_in_trouts = dad_caught - caleb_caught

    # FA
    answer = difference_in_trouts
    return answer