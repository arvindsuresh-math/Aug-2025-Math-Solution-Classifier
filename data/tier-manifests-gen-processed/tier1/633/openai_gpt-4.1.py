def solve():
    """Index: 633.
    Returns: the combined shoe size for Jasmine and Alexa.
    """
    # L1
    jasmine_size = 7 # Jasmine's shoe size is 7
    multiplier = 2 # Alexa's shoes are twice as big as Jasmine's shoes
    alexa_size = jasmine_size * multiplier

    # L2
    total_shoe_size = alexa_size + jasmine_size

    # FA
    answer = total_shoe_size
    return answer