# Calculate Sally's daily savings
sally_daily_savings = 0.5 * 6

# Calculate Sally's yearly savings
sally_yearly_savings = sally_daily_savings * 365

# Calculate Bob's daily savings
bob_daily_savings = 0.5 * 4

# Calculate Bob's yearly savings
bob_yearly_savings = bob_daily_savings * 365

# Calculate the total savings for the trip
total_savings = sally_yearly_savings + bob_yearly_savings

# Print the result
print(f"Sally's daily savings: ${sally_daily_savings}")
print(f"Sally's yearly savings: ${sally_yearly_savings}")
print(f"Bob's daily savings: ${bob_daily_savings}")
print(f"Bob's yearly savings: ${bob_yearly_savings}")
print(f"Total savings: ${total_savings}")