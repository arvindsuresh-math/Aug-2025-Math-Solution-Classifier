# Calculate the cost of shorts
num_shorts = 5
cost_per_short = 7
total_cost_shorts = num_shorts * cost_per_short

# Calculate the cost of shoes
num_shoes = 2
cost_per_shoe = 10
total_cost_shoes = num_shoes * cost_per_shoe

# Calculate the combined cost of shorts and shoes
total_cost_shorts_shoes = total_cost_shorts + total_cost_shoes

# Calculate the remaining amount spent on tops
total_spent = 75
remaining_for_tops = total_spent - total_cost_shorts_shoes

# Calculate the cost per top
num_tops = 4
cost_per_top = remaining_for_tops / num_tops

# Print the result
print(cost_per_top)