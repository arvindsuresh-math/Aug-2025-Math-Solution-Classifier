def solve():
    """Index: 865.
    Returns: the number of chocolate squares Jenny ate.
    """
    # L1
    mike_ate = 20 # If Mike ate 20 chocolate squares
    multiplier_thrice = 3 # WK
    thrice_mike_ate = mike_ate * multiplier_thrice

    # L2
    more_than_thrice = 5 # ate 5 more
    jenny_ate = thrice_mike_ate + more_than_thrice

    # FA
    answer = jenny_ate
    return answer