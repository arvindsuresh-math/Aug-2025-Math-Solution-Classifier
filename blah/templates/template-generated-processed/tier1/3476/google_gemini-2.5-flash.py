def solve():
    """Index: 3476.
    Returns: Hannah's age.
    """
    # L1
    num_brothers = 3 # three brothers
    brother_age = 8 # all 8 years old
    sum_brothers_ages = num_brothers * brother_age

    # L2
    multiplier_twice = 2 # twice as old
    hannah_age = sum_brothers_ages * multiplier_twice

    # FA
    answer = hannah_age
    return answer