def solve():
    """Index: 170.
    Returns: Mel's age in years when Katherine is two dozen years old.
    """
    # L1
    num_dozen = 2 # 2 dozen years old
    dozen = 12 # WK
    katherine_age = num_dozen * dozen

    # L2
    mel_younger = 3 # Mel is three years younger than Katherine
    mel_age = katherine_age - mel_younger

    # FA
    answer = mel_age
    return answer