def solve():
    """Index: 2339.
    Returns: the total number of shirts Hazel and Razel have.
    """
    # L1
    hazel_shirts = 6 # Hazel received 6 shirts
    multiplier_twice = 2 # twice the number of shirts
    razel_shirts = hazel_shirts * multiplier_twice

    # L2
    total_shirts = razel_shirts + hazel_shirts

    # FA
    answer = total_shirts
    return answer