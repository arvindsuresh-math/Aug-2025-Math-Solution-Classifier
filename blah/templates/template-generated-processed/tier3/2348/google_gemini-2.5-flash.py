def solve():
    """Index: 2348.
    Returns: the number that is fifteen more than a quarter of 48.
    """
    # L1
    total_number = 48 # 48
    quarter_divisor = 4 # a quarter of
    quarter_of_48 = total_number / quarter_divisor

    # L2
    fifteen_more = 15 # fifteen more
